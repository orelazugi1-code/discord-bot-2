function t(lang, key, vars = {}) {
  let s = S[lang]?.[key] ?? S.he[key] ?? key;
  if (typeof s !== 'string') return s;
  for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, String(v));
  return s;
}

